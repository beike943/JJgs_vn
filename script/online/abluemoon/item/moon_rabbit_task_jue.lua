--==================================ฝลฑพึ๗ย฿ผญว๘=============================
function OnUse(goods_index)
	local goods_id = GetItemParticular(goods_index)
	local goods_diff = 0
	for i =1, getn(jue_id) do
		if goods_id == jue_id[i][1] then
			goods_diff = i
			break
		end
	end
	Say("S๖ d๔ng t๓i b kiีp sฯ may mพn nhหn ฎญ๎c cธc loนi yีu quyีt mซn phธi. S๖ d๔ng?",2,
	"งๅng !/#goods_use_det("..goods_diff..")",
	"Chญa cวn/end_dialog"
	)
end

function end_dialog()
end

--***********************************ฮ๏ฦทสนำรว๘******************************
function goods_use_det(goods_diff)
	if Zgc_pub_goods_add_chk(5,50) ~= 1 then		--ฟีผไ/ธบึุละถฯ
		Talk(1,"","ว๋ศทศฯฤ๚ตฤฑณฐำะืในปตฤธบึุบอฟีผไฃก")
		return
	end
	if GetItemCount(2,0,jue_id[goods_diff][1]) == 0 then
		Talk(1,"","ว๋ศทศฯฤ๚ตฤฐนึะำะ<color=red>"..jue_id[goods_diff][2].."<color>ฃก")
		return
	end
	if DelItem(2,0,jue_id[goods_diff][1],1) ~= 1 then
		Talk(1,"","ว๋ศทศฯฤ๚ตฤฐนึะำะ<color=red>"..jue_id[goods_diff][2].."<color>ฃก")
		return
	else
		for i = 1, 5 do
			local ran_num = random(1,getn(jue_list[goods_diff]))
			local add_flag = AddItem(jue_list[goods_diff][ran_num][2],jue_list[goods_diff][ran_num][3],jue_list[goods_diff][ran_num][4],1)
			if add_flag == 1 then
				Msg2Player("ฤใป๑ตราปฑพ:\""..jue_list[goods_diff][ran_num][1].."\"")
			else
				WriteLog(GetName().."ป๑ตรฝฑภ๘ฃบ"..jue_list[goods_diff][ran_num][1].."ิ๖ผำสงฐฃฌสงฐฑ๊ึพฃบ"..add_flag)
			end
		end
	end	
end
