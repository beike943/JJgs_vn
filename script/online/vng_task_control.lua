function GetBitExt(nTask,nBitStart,nBitEnd)
	if (nBitStart > nBitEnd ) or ( nBitStart <1 ) or (nBitEnd > 32) then
		return 0;
	end;

	local Value = 0;
	local iBit = 1 ;
	local iTask = GetTask(nTask);

	for i = nBitStart,nBitEnd do
		Value = SetBit(Value, iBit,GetBit(iTask,i));
		iBit = iBit + 1;
	end;
	return Value;
end;

