function ActionPoint(param)
	local _,_, loc, act = strfind(param, "(.+),(.+)");

	local tLoc = getLocation(loc)
	if not tLoc then
		return
	end
--	setorigenAI(act, loc)
	MoveTo(tLoc[1], tLoc[2], tLoc[3]);
end

function getLocation(loc)
	local tLoc = tActionLocation[loc];
	if not tLoc then return end
	if type(tLoc[1]) ~= 'number' then
		local map,_,_ = CL_GetWorldPos();
		local nMap, nMin = 0, 9999;
		for k,v in tLoc do
			if abs(k - map) < nMin then
				nMap, nMin = k, abs(k-map)
			end
		end
		if not nMap then return end
		tLoc = tLoc[nMap];
	end
	return tLoc;
end

nCurPoint = 1
keys = {}
for k,v in tActionLocation do
	keys[nCurPoint] = k;
	nCurPoint = nCurPoint + 1;
end
nCurPoint = 1

function processAllPoint()
	local f = openfile("d:\\location_err.txt", 'w')
	while nCurPoint <= getn(keys) do
		tLoc = tActionLocation[keys[nCurPoint]];
		if not tLoc then return end

		if type(tLoc[1]) == 'number' then
			if MoveTo(tLoc[1], tLoc[2], tLoc[3]) ~= 0 then
				write(f, "错误地点: "..keys[nCurPoint].."\n");
			end
		else
			for k,v in tLoc do
				if MoveTo(v[1], v[2], v[3]) ~= 0 then
					write(f, "错误地点: "..keys[nCurPoint]..":"..k.."\n");
				end
			end
		end
		nCurPoint = nCurPoint + 1;
	end
	closefile(f)
end
